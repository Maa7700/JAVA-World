package ControlStatement;

public class Switch_Case {

	public static void main(String[] args) {
		
		int i=10;
		
		switch(i)
		{
		
		case 300:System.out.println("Enter value accurate 10");
		        break;
		
		case 20:System.out.println("Enter value Wrong");
		
		default:System.out.println("Publish Any thing default");
		

	}

}
	
}

/* Switch case mostly allows a variable to be tested equally against list of values
 Here value is called as case and variable switched to check each case.*/
